            "............................\n" +
            "............................\n" +
            "..................B.........\n" +
            "............................\n" +
            "............B...............\n" +
            "............................\n" +
            "......B.....................\n" +
            "............................\n" +
            "..B.........................\n" +
            "##.........................."
        ));

        // ── 3. 多层陷阱走廊 ──
        // 地刺 + 火焰 + 摆锤组合，需要观察节奏穿越
