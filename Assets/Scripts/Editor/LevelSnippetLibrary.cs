///
/// 物理约束速查（来自 PhysicsMetrics.cs）：
///   MAX_JUMP_HEIGHT = 2.5 格（原地最高跳跃）
///   MAX_JUMP_DISTANCE = 4.5 格（满速平跳最大水平距离）
