        // S35 修复: 弹跳平台间距调整为物理可达，上方留出 3 格安全净空
        // S43 修复: 弹跳平台水平间距从 8 格缩小到 4 格（ASCII_MAX_GAP=4），
        //          垂直间距从 2 格缩小到 1 格，确保物理可达。
        //          原始设计间距 8 格远超 MAX_JUMP_DISTANCE=4.5 格，物理上不可能跨越。
        snippets.Add(new Snippet(
